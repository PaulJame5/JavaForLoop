class Main 
{
  public static void main(String[] args) 
  {

    //Code of Java for loop  
    for(int i=1; i<=10; i++)
    {  
      for(int j = 1; j <= i; j++)
      {
        System.out.print(j); 
      }  
      // Create New Line
      System.out.println("");
    } // End for loop i++
    
    //Code of Java for loop  
    for(int i=10; i>=1; i--)
    {  
      for(int j = 1; j <= i; j++)
      {
        System.out.print(j); 
      }  
      System.out.println("");
    } // end for loop i--
  } // end main(args)
} // end class Main
